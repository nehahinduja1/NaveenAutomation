package JavaBascis;

public class Data_types {
  public static void main (String[]args) {
      int i = 10;
      double d = 34.25;
      double d1 = 100;
      float f = 3.14f;
      char c = 'A';
      char c1 = '$';
      char c2 = '3';
      String s = "Hi World, this is my first program";  // string is a class, not a data type but can be used as data type
      long l = 3000000000000000000L;
      boolean b1 = true;
      boolean b2 = false;   // true and false are values as well as keyword in Java
      System.out.println(i);
      System.out.println(d);
      System.out.println(f);
      System.out.println(c1);
      System.out.println(c2);
      System.out.println(s);
      System.out.println(l);
      System.out.println(b1);
      System.out.println(b2);
  }
}