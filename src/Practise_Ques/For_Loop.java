package Practise_Ques;

public class For_Loop {

    // write a program to print the integer number from 10 to 1
    public static void main(String[] args) {
        for (int i = 10; i >= 1; i--) {    // 10; 10 >= 1
            System.out.println(i);         // print 10; 10-1 = 9

                                         // i = 9; 9 >= 1
                                        // print 9; 9-1 = 8
        }
    }
}
